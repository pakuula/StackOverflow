def get_prime_range(bits: int) -> Tuple[int, int]:
    """Возвращает минимальное и максимальное простое число для заданного количества битов.

    :param bits: количество битов
    :return: кортеж (min_prime, max_prime)
    """
    if bits < 2:
        raise ValueError("Количество битов должно быть не меньше 2.")
    if bits in primes_2:
        return primes_2[bits]
    min_prime = next_prime(1 << (bits - 1), direction=Direction.FORWARD)
    max_candidate = (1 << bits) - 1
    max_prime = next_prime(max_candidate, direction=Direction.BACKWARD)
    primes_2[bits] = (min_prime, max_prime)
    return (min_prime, max_prime)
