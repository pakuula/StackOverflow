"""Список простых чисел для использования в проверках простоты числа."""

from enum import Enum
from .primality import is_prime_miller_rabin
from typing import Tuple


class Direction(Enum):
    """Направление поиска простого числа."""

    FORWARD = 1
    BACKWARD = -1


def next_prime(start: int, direction: Direction = Direction.FORWARD) -> int:
    """Возвращает следующее простое число, большее чем start.

    :param start: число, больше которого нужно найти простое число
    :return: следующее простое число
    """
    if start < 2:
        raise ValueError("Число должно быть больше или равно 2.")
    if start == 2 and direction == Direction.BACKWARD:
        raise ValueError("Нет простых чисел меньше 2.")
    if direction == Direction.BACKWARD:
        if start == 3:
            return 2
        candidate = start - 2 if start % 2 == 1 else start - 1
    else:
        candidate = start + 2 if start % 2 == 1 else start + 1

    max_small_prime = primes_500[-1]
    max_small_square = max_small_prime * max_small_prime

    def _basic_check(n: int) -> bool:
        for p in primes_500:
            if p * p > n:
                return True
            if n % p == 0:
                return False
        return True

    while candidate <= max_small_square and candidate >= 2:
        if _basic_check(candidate):
            return candidate
        candidate += 2 * direction.value
    if candidate < 2:
        raise ValueError("Нет простых чисел меньше 2.")
    while True:
        if is_prime_miller_rabin(candidate):
            return candidate
        candidate += 2 * direction.value

def next_prime_forward(start: int) -> int:
    """Возвращает следующее простое число, большее чем start."""
    return next_prime(start, direction=Direction.FORWARD)

def next_prime_backward(start: int) -> int:
    """Возвращает следующее простое число, меньшее чем start."""
    return next_prime(start, direction=Direction.BACKWARD)

